XY MapPosToIndex(XY pos)	// pos = mvedPos
{
	XY mapIndex;

	mapIndex = { pos.x / CHIP_SIZE_X, pos.y / CHIP_SIZE_Y };

	return mapIndex;
}

bool IsPass(XY pos)
{
	bool ret = true;
	int mapNo;
	XY mapIndex;

	mapIndex = MapPosToIndex(pos);
	mapNo = mapData[mapIndex.y][mapIndex.x];

	switch (mapNo)
	{
	case 0:
	case 1:
	case 2:
	case 3:
		ret = false;
		break;
	}
	return ret;
}

// 壁にぴったりくっつくようにする
XY MapIndexToPos(XY index)
{
	XY mapPos;

	mapPos = { index.x * CHIP_SIZE_X, index.y * CHIP_SIZE_Y };

	return mapPos;
}

XY MapPos(XY pos, MOVE_DIR der) {
	XY tmpIndex;
	tmpIndex = MapPosToIndex(pos);
	switch (der)
	{
	case DIR_UP:
		tmpIndex.y;
		break;
	case DIR_RIGHT:
		tmpIndex.x++;
		break;
	case DIR_DOWN:
		tmpIndex.y++;
		break;
	case DIR_LEFT:
		tmpIndex.x;
		break;
	}
	return MapIndexToPos(tmpIndex);
}
